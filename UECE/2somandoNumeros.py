# Solicitando dois números ao usuário:
numero1 = float(input("Digite um número: "))
numero2 = float(input("digite um outro número: "))

# Somando os dois números:
soma = numero1 + numero2

# Imprimindo na tela do usuário
# Para formatar strings, usamos o f-strings:
print(f"A soma entre {numero1} e {numero2} é {soma}")
